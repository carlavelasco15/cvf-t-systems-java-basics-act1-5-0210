package ejercicio3;

public class Ejercicio3 {

	public static void main(String[] args) {
		int X = 13;
		int Y = 42;
		double N = 3.14;
		double M = 1.17;
		
		System.out.println("VALORES DE LAS VARIABLES");
		System.out.println("X = " + X);
		System.out.println("Y = " + Y);
		System.out.println("N = " + N);
		System.out.println("M = " + M);
		System.out.println("");
		
		System.out.println("X + Y = " + (X+Y));
		System.out.println("X - Y = " + (X-Y));
		System.out.println("X * Y = " + (X*Y));
		System.out.println("X / Y = " + (X/Y));
		System.out.println("X % Y = " + (X%Y));
		System.out.println("");
		
		System.out.println("N + M = " + (N+M));
		System.out.println("N - M = " + (N-M));
		System.out.println("N * M = " + (N*M));
		System.out.println("N / M = " + (N/M));
		System.out.println("N % M = " + (N%M));
		System.out.println("");
		
		
		System.out.println("EL DOBLE DE CADA VARIABLE");
		System.out.println("2 x X = " + (2*X));
		System.out.println("2 x Y = " + (2*Y));
		System.out.println("2 x N = " + (2*N));
		System.out.println("2 x M = " + (2*M));
		System.out.println("");
		
		System.out.println("LA SUMA DE TODAS LAS VARIABLES");
		System.out.println("X + Y + N + M = " + (X+Y+N+M));
		System.out.println("");
		
		System.out.println("EL PRODUCTO DE TODAS LAS VARIABLES");
		System.out.println("X + Y + N + M = " + (X*Y*N*M));
		System.out.println("");
		
	}

}
