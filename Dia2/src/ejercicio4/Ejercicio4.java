package ejercicio4;

public class Ejercicio4 {
	
	public static void main(String[] args) {
		int N = 33;
		System.out.println("Valor inicial: " + N);
		
		
		N += 77;
		System.out.println("N + 77 = " + N);
		
		N -= 3;
		System.out.println("N - 3 = " + N);
		
		N *= 2;
		System.out.println("N * 2 = " + N);
	}
	
	
	

}
